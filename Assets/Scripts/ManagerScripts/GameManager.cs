using System;
using UnityEngine;

public class GameManager : MonoBehaviour
{
    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    private static GameManager _instance;

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    public static GameManager Instance
    {
        get
        {
        
            if (_instance == null)
            {
         
                _instance = FindObjectOfType<GameManager>();

        
                if (_instance == null)
                {
                    GameObject singletonObject = new GameObject(typeof(GameManager).Name);
                    _instance = singletonObject.AddComponent<GameManager>();
                }
            }

            return _instance;
        }
    }

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    public static event Action<GameState> OnGameStateChanged;
    public static event Action<AddScore> OnScoreStateChanged;

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    public GameState state;
    public AddScore score;

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    public int numberOfBlocksAttech = 1;
    public int multiplier = 0;
    public int totalScore;

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    private bool isGameStarted = false;

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    private void Awake()
    {
  
        if (_instance == null)
        {
            _instance = this;
            DontDestroyOnLoad(gameObject); 
        }
        else
        {
 
            if (_instance != this)
            {
                Destroy(gameObject);
            }
        }
    }


    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    private void Start()
    {

        Application.targetFrameRate = 90;
        totalScore = 0;
    }

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    private void Update()
    {
        if (Input.GetMouseButtonDown(0))
        {
            if (!isGameStarted)
            {
                isGameStarted = true;
                UpdateGameState(GameState.playing);

            }
        }
    }


    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    public void  UpdateGameState(GameState newState)
    {
        state = newState;

        switch (newState)
        {
            case GameState.mainmenu:
                totalScore = 0;
                isGameStarted = false;
                break;
            case GameState.playing:
                break;
            case GameState.restart:
                break;
            case GameState.bonus:
                break;
            case GameState.finish:
                break;
        }

        OnGameStateChanged?.Invoke(newState);

    }

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    public void UpdateScoreState(AddScore newAddScore)
    {
        score = newAddScore;

        switch (newAddScore)
        {
            case AddScore.addScore:
                totalScore++;
                break;
            case AddScore.multiplierScore:
                totalScore *= multiplier;
                break;
        }

        OnScoreStateChanged?.Invoke(newAddScore);

    }

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//

    private void OnDestroy()
    {

        if (_instance == this)
        {
            _instance = null;
        }
    }

    //--------------------------------------------------------------------------------------------------------------------------------------------//
    //--------------------------------------------------------------------------------------------------------------------------------------------//
}

//--------------------------------------------------------------------------------------------------------------------------------------------//
//--------------------------------------------------------------------------------------------------------------------------------------------//

public enum GameState
{
    mainmenu,
    playing,
    restart,
    bonus,
    finish,

}

//--------------------------------------------------------------------------------------------------------------------------------------------//
//--------------------------------------------------------------------------------------------------------------------------------------------//

public enum AddScore
{
    addScore,
    multiplierScore

}
